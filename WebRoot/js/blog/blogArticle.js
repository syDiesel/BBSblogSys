$(document).ready(function(){ 


$('#tdtwBtn').click(function(){
	$('#tdtwInfo').css('display','block');
	$('#tdhdInfo').css('display','none');
	$('#tdtwBtn').css('background','#087ec4');
	$('#tdtwBtn a').css('color','#fff');
	$('#tdhdBtn').css('background','#fff');
	$('#tdhdBtn a').css('color','#333');
	$('#wendaANDtiwen').css('display','block');
});
 
$('#tdhdBtn').click(function(){
	$('#tdtwInfo').css('display','none');
	$('#tdhdInfo').css('display','block');
	$('#tdhdBtn').css('background','#087ec4');
	$('#tdhdBtn a').css('color','#fff');
	$('#tdtwBtn').css('background','#fff');
	$('#tdtwBtn a').css('color','#333');
	$('#wendaANDtiwen').css('display','none');
});

});




