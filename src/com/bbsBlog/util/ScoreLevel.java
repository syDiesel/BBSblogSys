package com.bbsBlog.util;

public class ScoreLevel {

	public static String scoreLevel(String score){
		double score0=Double.parseDouble(score);
		String level = null;
		if(score0<10)
			level="0";
		else if(score0>=10&&score0<100)
			level="1";
		else if(score0>=100&&score0<1000)
			level="2";
		else if(score0>=1000&&score0<10000)
			level="3";
		else if(score0>=10000&&score0<100000)
			level="4";
		else if(score0>=100000&&score0<1000000)
			level="5";
		else if(score0>=1000000&&score0<10000000)
			level="6";
		else if(score0>=10000000)
			level="7";
		
		return level;
		    
	}
}
